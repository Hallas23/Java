package exercise3;

public class ElApp extends Item {
	
	

	public ElApp (double netprice, String name) {
			this.setNetprice(netprice);
			this.setName(name);
	}
	
	public double calcVAT() {
		
		if ((getNetprice() * 1.30) - getNetprice() > 3) {
		return (getNetprice() * 1.30) - getNetprice() ;
		}
		return 3;
	}


}
